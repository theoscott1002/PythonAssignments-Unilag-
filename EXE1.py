# 1) Band Name Generator
# ● Ask user for the name of the city they grew up in
# ● Ask user for their pet’s name
# ● Print: Your band name could be (a combination of the city and pet name using
# concatenation).

cityName = str(input("What is the name of the city you grew up in:"))
petName = str(input("What is your pet's name:"))
print("Your band name could be:" + cityName + petName)
